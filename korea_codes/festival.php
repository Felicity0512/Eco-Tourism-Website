    <h3>SUNDUAN</h3>
    <p>“Sunduan” is a street pageant tradition dating back to the 16th century. The term “sunduan” is derived from the word sundo which literally means “to fetch” and relates to an old courtship custom of waiting on one’s lady love, accompanying her to the town plaza, and bringing her home. It conveys the message that Filipinos are romantic and that their womenfolk are regarded with love and respect. This also depicts chivalry (pagka-maginoo) of the men folk. This is participated in by members of the sangguniang bayan, the barangay captains and their spouses and other prominent members of Binangonan society.</p>
