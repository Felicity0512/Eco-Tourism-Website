    <p>Traditionally held during the Holy week, particularly every Good Friday, the Giwang-Giwang has evolved into a more elaborate observance. Following the traditions of the burial of the dead, the Giwang-Giwang procession goes around town where the image of the Santo Entierro is being hoisted on shoulders of thousands of devotees, making the image “sway“, thus the term “Giwang-Giwang” or “swaying“.</p>
