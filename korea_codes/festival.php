    <p>A relatively new tradition, this evolved from the Giwang-Giwang observance during Good Fridays. Because of the danger posed by the procession to young children, they were not allowed to participate during the actual procession. Because of this, children from Regidor St. in Brgy. Layunan came up with their own version of the “Giwang-Giwang“. They produced small images of saints and holy images much like the ones being used in the procession and proceeded to stage a procession much like the one being done by their elders. In 2013, the Sta. Ursula Parish Church adopted the tradition and educated the young children who own these small holy images on the religious implications and significance of the celebration.</p>
