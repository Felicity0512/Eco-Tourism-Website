         <h3>KALBARYO</h3>
         <p>Address: Brgy. Libid, Binangonan Rizal</p>
         <p><a href="https://www.google.com.ph/maps/place/Ang+Kalbaryo/@14.4643109,121.1939326,18z/data=!4m5!3m4!1s0x3397c3a310777d37:0xb8e5d7df75be4fee!8m2!3d14.4634253!4d121.1975375?shorturl=1">View map</a></p>

         <p>Mt. Calvary can be found in Barangay Libid, Binangonan, Rizal. Just a few minutes ride from the townâ€™s Parish Church or 30 minutes walk from the town proper up to the top of the Mt. Calvary (Kalbaryo). Climbing it is both fantastic and very fulfilling experience. With more than 288 steps, anyone can appreciate a majestic view of the town and the Lake, accentuated by the image of the Blessed Virgin and a large Cross at the top of the hill. The Cross stands 15 feet from its base which can be seen by the majority of the townsfolk. In front of it rests the image of the Virgin Mary encased in a thick transparent glass display. The two symbolic (or sacred) landmarks are regularly maintained. </p>
         </br>
         <p>The Cross is known not only for its religious significance, but also for its historical value. Legend has it that a cross-shaped rock suddenly grew a top the hill but was subsequently destroyed by lightning and earthquake. To commemorate the supernatural occurrence, the people of the town built a wooden cross in 1972 which was then replaced by steel-made material which still stands today. In the same hill, an old chapel was once built but only its ruins remain at present. According to the local officials, approximately 44,000 pilgrims visit this religious place during the holy week.</p>
         </br>
         <p>Pilgrims, tourists, and locals never falter or even get tired of ascending the Calvary trail, knowing that the experience is very worthwhile. The feel of fresh air blowing highlighted by the calm and serene ambience of the place is just an unbelievable pleasure.</p>
         </br>
         <p>The stunningly panoramic scenery of the town area and the Laguna de Bay provides relief and viewing bonus to all visitors.</p>
