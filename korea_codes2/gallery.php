    <h3>PETROGLYPHS</h3>
    <p>Located at the boundary of Angono and Binangonan, the Petroglyphs was discovered by National Artist Carlos “Botong” Francisco in 1965. This cultural heritage site was certified by National Museum as dating back to circa 3000 B.C.; and is the most ancient Filipino relic. There are about 127 drawings of animal and human figures engraved in the rock. Previous archeological findings yielded fragment of earthen ware, obsidian flakes and shells.</p>
    <p>The Angono-Binangonan Petroglyphs has been included in the World Inventory of Rock Art under the auspices of UNESCO, ICCROM and ICOMOS. It has been declared as a National Cultural treasure by virtue of Presidential Decree No. 260. In 1996, it received its highest recognition when it was nominated as one of the “100 Most Endangered Site of the World” under the World Monument Watch List. The preservation and development of the Petroglyphs is a joint effort of the National Museum of the Philippines, World Monument Watch Fund, American Express International, Department of Tourism and Antipolo Properties.</p>
